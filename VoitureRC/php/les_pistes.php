    <h1>Présentation de quelques circuits:</h1>

    <h2><span style="text-decoration: underline;">La piste Laurent:</span></h2>

    <img src="../imgs/La_piste_laurent.jpg" alt="Piste Laurent" id="piste_Laurent"/>

      <p id="espace_image_1">
        Ce circuit est une piste indoor en bitume d'un developpé de 200m sur laquelle est organisé des manches de Championnat de France, les course amicale et de ligue.
        Le record du tour sur cette piste est inféreur à 13s.
        <br>Au niveau des stands, ils sont couverts, chauffés, permettent d'accueillir jusqu'a 50 pilotes avec un bon éclairage et des tableaux électriques avec un distributeur de boissons.</br>
     </p>

     <h2><span style="text-decoration: underline;">La piste de DMT Court Circuit</span></h2>

     <img src="../imgs/DMT_Court_Circuit.jpg" alt="Piste DMT court circuit" id="piste_DMT"/>

    <p id="espace_image_2">
    Situé à Court dans le Jura Bernois, à 5 km à vol d’oiseau de Moutier.
    La piste est réservée aux voitures électriques et est pleinement opérationnelle. 
    <br>Elle est située dans un ancien bâtiment industriel dédié au décolletage, très lumineux, aussi bien en journée grâce aux grandes surfaces vitrées qu'en soirée, grâce à un éclairage bien implanté.</br>
    Le tracé, grâce à un balisage modulable, évolue régulièrement. La moquette grise procure d'emblée un bon grip aisni que pour les surfaces.
    </p>

    <h2><span style="text-decoration: underline;">La piste de Auto R/C du Faucigny</span></h2>

    <img id="piste_faucigny" src="../imgs/auto_RC_du_Faucigny.PNG" alt="Piste Auto R/C du Faucigny">

    <p id="espace_image_3">
    Rendez-vous était donné à tous les mordus du 1/12° pour des manches du Championnat de France 1/12°. Tout était donc prévu par ses bénévoles pour bien accueillir les pilotes.
    La piste indoor permanente de ce club se situe à Bonneville, en Haute-Savoie, au quartier du Bouchet, derrière la gare, dans les locaux d'une ancienne usine désaffectée.
    Point apprécié des pilotes, la salle était efficacement chauffée.
    La piste en moquette grise a un développé de 110m de long pour une largeur comprise entre 2.30 et 3.00m. 
    Le tracé est plutôt technique et se montre exigeant en terme de pilotage et de mise au point châssis.
    Le balisage est en lates biseautées blanches bien visibles et des portions de moquette verte signalent le "gazon".
    Un virage relevé est présent avant la plus grande des lignes droites.
    Il fallait rester sur la trajectoire, matérialisée sur la piste par une trace noire, pour bénéficier du grip maximum. Comme la piste est relativement bosselée, il fallait tenir compte de ce paramètre los des réglages de son auto.
    </p>
    
